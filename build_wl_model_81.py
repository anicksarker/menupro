#!/usr/bin/env python3
# =============================================================================
#  build_wl_model_81.py   (Plan 81 - Paid-Up Whole Life)
# -----------------------------------------------------------------------------
#  Rebuilds the workbook from ONLY two CSV inputs:
#         1) Inputs.csv     (label,value  - one policy)
#         2) cso_rates.csv  (mortality rate matrix)
#
#  Design (same as the Plan-34 builder):
#    * 12 sheets: CSO Rates, Inputs, MORTALITY, Commutation, CV_RATES,
#      PUA_TABLE, DIvidend_Assumption, Projection_Monthly, RPU, ETI, APL, Loan.
#    * CSO Rates  = cso_rates.csv read into a DataFrame and pasted as-is
#                   (only row 4's CONCAT codes are formulas). NOTHING hardcoded.
#    * Inputs     = keeps ONLY columns A, B, D, E (description/legend columns
#                   C and F..S are dropped). Column B is driven by Inputs.csv,
#                   so a different policy just runs with its own numbers.
#    * every other sheet is pure formulas referencing those two sheets.
#
#  NOTE - external link fix:
#    The reference file's MORTALITY column C used '[2]ETI!$B$7' - a link to a
#    SECOND workbook (the Plan-34 file), which discounted at 2.5% instead of
#    this policy's own 2.75%. A self-contained model cannot resolve an external
#    file, so every '[2]ETI!$B$7' has been localized to 'ETI!$B$7' (this
#    workbook), i.e. it now discounts at the policy's own accumulation rate.
#
#  Repeated (fill-down) formulas are written with a LOOP: for each column the
#  origin formula is translated down every row exactly like Excel fill-down
#  (relative refs shift, absolute $refs stay, integer counters increment).
#
#  MODEL_SPEC per sheet:
#     "statics"  : {coord: value}            labels, headers, constants
#                                            (CSO Rates has NONE - it's the CSV)
#     "arrays"   : [coords]                  cells entered as array formulas
#     "fills"    : [ [col, r0, r1, kind, origin_formula, counter_positions] ]
#         kind "trans"   : fill origin_formula from r0 down to r1 (refs shift)
#         kind "counter" : same, then bump integer literal(s) at the given
#                          positions by (row - r0)   e.g.  +1-1, +2-1, +3-1 ...
#         kind "lit"     : write origin_formula verbatim (array formulas)
#     "colfmt"/"fmt_exc" : number formats      "widths": column widths
#  Dates in "statics" are stored as ["__dt__", "ISO"] so they round-trip.
#
#  Usage:
#     pip install openpyxl pandas
#     python build_wl_model_81.py                                  # defaults
#     python build_wl_model_81.py Inputs.csv cso_rates.csv out.xlsx
# =============================================================================
