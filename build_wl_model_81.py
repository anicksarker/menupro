CSO_CSV    = sys.argv[2] if len(sys.argv) > 2 else "cso_rates.csv"
OUT_XLSX   = sys.argv[3] if len(sys.argv) > 3 else "WL_Model_Paidup_81.xlsx"

SHEET_ORDER = ['CSO Rates','Inputs','MORTALITY','Commutation','CV_RATES','PUA_TABLE',
               'DIvidend_Assumption','Projection_Monthly','RPU','ETI','APL','Loan']

# =============================================================================
#  Fill / helper engine
# =============================================================================
_INT = re.compile(r'(?<![A-Za-z$0-9_.])\d+')

def _bump_ints(s, positions, delta):
    out, last = [], 0
    for i, m in enumerate(_INT.finditer(s)):
        if i in positions:
            out.append(s[last:m.start()]); out.append(str(int(m.group()) + delta)); last = m.end()
    out.append(s[last:]); return ''.join(out)

def _translate(formula, origin, dest):
    """Excel fill-down: shift relative refs from origin cell to dest cell."""
    return Translator(formula, origin=origin).translate_formula(dest)

def _dec(v):
    if isinstance(v, list) and len(v) == 2 and v[0] == "__dt__":
        return datetime.datetime.fromisoformat(v[1])
    return v

def _cell(x):
    """Type a raw CSV string: '' -> None, integer -> int, else float, else str."""
    if x is None: return None
    x = str(x).strip()
    if x == "" or x.lower() == "nan": return None
    try:
        i = int(x)
        if str(i) == x: return i
    except ValueError: pass
    try: return float(x)
    except ValueError: return x

def _coerce(raw, ref):
    """Coerce a CSV input string to match the reference cell's python type."""
    raw = (raw or "").strip()
    if raw == "": return None
    if isinstance(ref, list) and len(ref) == 2 and ref[0] == "__dt__":            # date
        for fmt in ("%d-%m-%Y", "%Y-%m-%d", "%m/%d/%Y", "%d/%m/%Y"):
            try: return datetime.datetime.strptime(raw, fmt)
            except ValueError: pass
        return raw
    if isinstance(ref, bool):
        return raw.upper() in ("TRUE", "1", "YES")
    pct = raw.endswith("%")
    num = raw[:-1] if pct else raw
    if isinstance(ref, float) or pct:
        try: return float(num) / 100.0 if pct else float(num)
        except ValueError: return raw
    if isinstance(ref, int):
        try: return int(float(num))
        except ValueError: return raw
    return raw                                                                    # string, e.g. "3802170L"

# =============================================================================
#  CSV loaders
# =============================================================================
def load_inputs(path):
    d = {}
    with open(path, newline="") as f:
        for row in csv.reader(f):
            if row and row[0].strip():
                d[row[0].strip()] = row[1] if len(row) > 1 else ""
    return d

def load_cso_df(path):
    return pd.read_csv(path, header=None, dtype=str, keep_default_na=False)

# =============================================================================
#  Sheet writer
# =============================================================================
def write_sheet(ws, name, spec, inputs=None, cso_df=None):
    written = set()
    def put(coord, value):
        ws[coord] = value
        written.add(coord)

    # ---- 1) CSO Rates : paste the DataFrame straight in (NOTHING hardcoded) --
    if name == "CSO Rates":
        for _, row in cso_df.iterrows():
            ws.append([_cell(v) for v in row.tolist()])
        for r in range(1, ws.max_row + 1):
            for c in range(1, ws.max_column + 1):
                if ws.cell(row=r, column=c).value is not None:
                    written.add(f"{get_column_letter(c)}{r}")
    else:
        # ---- 2) static cells (labels, headers, constants) -------------------
        for coord, val in spec["statics"].items():
            put(coord, _dec(val))

    # ---- 3) formula fills : LOOP each column's origin formula down its rows --
    arrays = set(spec.get("arrays", []))
    for col, r0, r1, kind, f0, pos in spec["fills"]:
        origin = f"{col}{r0}"
        for r in range(r0, r1 + 1):
            coord = f"{col}{r}"
            if kind == "lit":
                formula = f0
            elif kind == "trans":
                formula = _translate(f0, origin, coord)
            else:  # counter
                formula = _bump_ints(_translate(f0, origin, coord), pos, r - r0)
            put(coord, ArrayFormula(coord, formula) if coord in arrays else formula)

    # ---- 4) Inputs sheet : override column-B values from the policy CSV ------
    if name == "Inputs" and inputs is not None:
        for r in range(1, ws.max_row + 1):
            label = ws[f"A{r}"].value
            bcoord = f"B{r}"
            if label in inputs and bcoord in spec["statics"]:   # only genuine inputs
                v = _coerce(inputs[label], spec["statics"][bcoord])
                if v is not None:
                    ws[bcoord] = v

    # ---- 5) number formats + column widths ----------------------------------
    colfmt, exc = spec.get("colfmt", {}), spec.get("fmt_exc", {})
    for coord in written:
        col = re.match(r"[A-Z]+", coord).group()
        fmt = exc.get(coord, colfmt.get(col))
        if fmt:
            ws[coord].number_format = fmt
    for col, w in spec.get("widths", {}).items():
        ws.column_dimensions[col].width = w

# =============================================================================
def main():
    inputs = load_inputs(INPUTS_CSV)
    cso_df = load_cso_df(CSO_CSV)
    wb = Workbook(); wb.remove(wb.active)
    for name in SHEET_ORDER:
        print(f"  building sheet: {name}")
        write_sheet(wb.create_sheet(title=name), name, MODEL_SPEC[name], inputs=inputs, cso_df=cso_df)
    wb.save(OUT_XLSX)
    print(f"\nSaved: {OUT_XLSX}")
    print("Open in Excel to recalculate. Summarised outputs = Inputs!D2:E7")
    print("(Cash Value, PUA Cash Value, ETI, RPU, APL, Loan Outstanding).")


